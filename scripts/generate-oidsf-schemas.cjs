#!/usr/bin/env node
/**
 * Reads OIDSF unified YAML schemata and emits src/generated/oidsfBundledSchemas.ts
 * Run from plugin root: node scripts/generate-oidsf-schemas.cjs
 */

const fs = require('fs');
const path = require('path');
const { parse: parseYaml } = require('yaml');

const ROOT = path.resolve(__dirname, '..');
const SCHEMATA_DIR = path.join(ROOT, 'oidsf', 'spec', 'schemata');
const COLORS_PATH = path.join(__dirname, 'oidsf-schema-colors.json');
const OUT_FILE = path.join(ROOT, 'src', 'generated', 'oidsfBundledSchemas.ts');

const PALETTE = [
	'#607D8B',
	'#4CAF50',
	'#2196F3',
	'#FF9800',
	'#9C27B0',
	'#00BCD4',
	'#795548',
	'#E91E63',
	'#009688',
	'#3F51B5',
	'#FF5722',
	'#8BC34A',
	'#FFC107',
	'#673AB7',
	'#F44336',
	'#03A9F4',
	'#CDDC39',
	'#FF4081',
	'#00E676',
	'#7C4DFF',
	'#26C6DA',
	'#FF7043',
	'#78909C',
	'#A1887F',
];

function hashColor(name) {
	let h = 0;
	for (let i = 0; i < name.length; i++) h = (h * 31 + name.charCodeAt(i)) >>> 0;
	return PALETTE[h % PALETTE.length];
}

function normalizeExtends(body) {
	const e = body.extends;
	if (!e) return [];
	if (Array.isArray(e)) return e;
	return [e];
}

function stripSchemaNoise(body) {
	const out = { ...body };
	delete out.rdf;
	delete out.temporalExtent;
	delete out.edge;
	delete out.stix_family;
	delete out.arkham_family;
	return out;
}

function main() {
	const colorMap = JSON.parse(fs.readFileSync(COLORS_PATH, 'utf8'));
	const files = fs.readdirSync(SCHEMATA_DIR).filter((f) => f.endsWith('.yaml'));
	const bundled = {};

	for (const file of files.sort()) {
		const full = path.join(SCHEMATA_DIR, file);
		const raw = fs.readFileSync(full, 'utf8');
		const doc = parseYaml(raw);
		if (!doc || typeof doc !== 'object') {
			throw new Error(`Invalid YAML (empty): ${file}`);
		}
		const keys = Object.keys(doc);
		if (keys.length !== 1) {
			throw new Error(`Expected single top-level key in ${file}`);
		}
		const schemaName = keys[0];
		let body = stripSchemaNoise(doc[schemaName]);
		body = { ...body, extends: normalizeExtends(body), name: schemaName };
		const color = colorMap[schemaName] || hashColor(schemaName);
		body.color = color;
		bundled[schemaName] = body;
	}

	const banner = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/generate-oidsf-schemas.cjs — do not edit by hand.
 * Source: oidsf/spec/schemata/*.yaml + scripts/oidsf-schema-colors.json
 */
`;

	const json = JSON.stringify(bundled, null, '\t');
	const content = `${banner}
/** Bundled OIDSF unified schemata (FollowTheMoney-shaped). Typed in ftm-schema-service at import. */
export const OIDSF_BUNDLED_SCHEMAS = ${json};
`;

	fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
	fs.writeFileSync(OUT_FILE, content, 'utf8');
	console.log(`Wrote ${OUT_FILE} (${files.length} schemata)`);
}

main();
